public class ArrayUnidimensional {


    public static void main(String[] args) {
        String[] texto = new String[5];
        texto[0] = "Hola";
        texto[1] = "Adios";
        texto[2] = "Isla";
        texto[3] = "Arroz";
        texto[4] = "Blanco";

        for (int i = 0;i < texto.length;i++){
            System.out.println(texto[i]);
        }

    }
}
